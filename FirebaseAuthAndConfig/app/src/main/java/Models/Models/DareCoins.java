package Models;

public class DareCoins {

    private String mAmount;
    private String mPrice;

    public DareCoins(){}

    public String getAmount()
    {
        return mAmount;
    }

    public String getPrice()
    {
        return mPrice;
    }

    public void setAmount(String amount)
    {
        mAmount = amount;
    }

    public void setPrice(String price)
    {
        mPrice = price;
    }
}
